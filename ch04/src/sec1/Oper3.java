package sec1;

public class Oper3 {
	public static void main(String[] args) {
		//문자열 결합
		byte a='K',b='i',c='m';
			//char a='k',b='i',c='m'; 도 해보깅
		int d = 1004;
		System.out.println("d="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		System.out.println(a+b+c+1004);	//문자의 연산 결과는 숫자(문자열 결합일 때 문자로 환산/아래참고)
		System.out.println("lee"+d);	//문자열 결합
		
		//증감 연산자
		int k=1, l=1, m=1, n=1;
		System.out.println();
		System.out.println(k++);	//출력은 1이지만 값은 2로 바뀌어 있음(출력을 먼저 계산은 나중) -> 후위연산
		System.out.println(k);
		System.out.println(++l);	//계산도 2 출력도 2 (계산 먼저 하고 출력 ) -> 전위연산
		System.out.println(l);
		System.out.println(m--);	//출력은 1이지만 값은 0으로 바뀌어 있음	-> 후위연산
		System.out.println(m);
		System.out.println(--n);	//계산도도 0 출력도 0 (계산 먼저 하고 출력) -> 전위연산
		System.out.println(n);
		//대입 연산자
		int p=20, r=30;
		
		p+=2;	//p=p+2 -> 22
		r-=2;	//r=r-2 -> 28
		System.out.println(p);
		System.out.println(r);
		p*=2; // 44
		r/=2; // 14
		System.out.println(p);
		System.out.println(r);
	}

}
