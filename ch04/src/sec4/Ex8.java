package sec4;

public class Ex8 {

	public static void main(String[] args) {
		//이중 for 문을 활용하여 아래와 같은 형태의 구구단을 출력하시오.
		
/*		
	2*1=2 3*1=3,....................9*1=9
	2*2=4 3*2=6,.........................
	.....................................
	2*9=18.........................9*9=81	
*/
	
		for(int a=1;a<10;a++){				
			for(int b=2;b<10;b++){  //b가 2로시작해서 9까지 되면 다시 위에 for문의 a가 1에서 2로 증가한 다음에 b가  3으로 
				System.out.print(b+"*"+a+"="+(a*b)+"\t");		//
			}
			System.out.println();
		}

	}

}
